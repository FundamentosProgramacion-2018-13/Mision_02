# Descripcion: Programa que que calcula cierto tiempo y distancia de un automovil con respecto a su velocidad.

# Escribe tu programa después de esta línea.

Va = int (input("Teclea la velocidad del auto en km/h: "))
d7 = Va * 7
d45 = Va * 4.5
T = 791/Va

print ("La distancia que recorreas en 7 hrs sera: ", d7)
print ("La distancia que recorreras en 4.5 hrs sera: ", d45)
print ("El tiempo que tardaras en recorrer 791 km sera: ", T)
